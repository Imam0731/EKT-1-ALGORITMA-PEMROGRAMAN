# Tuple Example in Python

# Membuat tuple
angka = (10, 20, 30, 40, 50)
print("Tuple:", angka)

# Mengakses elemen
print("Elemen indeks ke-1:", angka[1])

# Slicing tuple
print("Slicing 1-3:", angka[1:4])

# Tuple bersifat immutable (tidak bisa diubah)
# angka[0] = 99  # Ini akan error

# Tuple dengan berbagai tipe data
campuran = ("Imam", 20, True, 3.14)
print("Tuple campuran:", campuran)

# Tuple dalam tuple (nested tuple)
nested = ((1, 2), (3, 4))
print("Nested tuple:", nested)

