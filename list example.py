# List Example in Python

# Membuat list
angka = [1, 2, 3, 4, 5]
print("List awal:", angka)

# Menambah elemen
angka.append(6)
print("Setelah append:", angka)

# Mengakses elemen
print("Elemen indeks ke-2:", angka[2])

# Menghapus elemen
angka.remove(3)
print("Setelah remove 3:", angka)

# Mengubah elemen
angka[0] = 10
print("Setelah mengubah elemen pertama:", angka)
